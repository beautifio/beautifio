/** Reverse mapping: benchmark slug (Indonesian) → template slug (English) */
export const BENCHMARK_TO_TEMPLATE_SLUG: Record<string, string> = 
  Object.fromEntries(
    Object.entries(TEMPLATE_TO_BENCHMARK_SLUG).map(([k, v]) => [v, k])
  );

export function getBenchmarkForTemplate(templateSlug: string): BenchmarkDream | undefined {
  const benchmarkSlug = TEMPLATE_TO_BENCHMARK_SLUG[templateSlug];
  if (!benchmarkSlug) return undefined;
  return BENCHMARK_DATABASE[benchmarkSlug];
}

export function getTemplateFromBenchmarkSlug(benchmarkSlug: string): string | undefined {
  return BENCHMARK_TO_TEMPLATE_SLUG[benchmarkSlug];
}

export function getBenchmarkDreamsByCategory(category: string): BenchmarkDream[] {
  return Object.values(BENCHMARK_DATABASE).filter((d) => d.category === category);
}

export function determineUserPhase(
  dreamSlug: string,
  userAge: number
): { phase: DreamPhase | null; allPhases: DreamPhase[]; status: "on_track" | "behind_schedule" | "ahead_of_schedule"; message: string } {
  const dream = BENCHMARK_DATABASE[dreamSlug];
  if (!dream) {
    return { phase: null, allPhases: [], status: "on_track", message: "Data mimpi tidak ditemukan." };
  }

  const allPhases = [...dream.phases].sort((a, b) => a.sort_order - b.sort_order);
  
  const currentPhase = allPhases.find(
    (p) =>
      (p.age_min === null || userAge >= p.age_min) &&
      (p.age_max === null || userAge <= p.age_max)
  ) || allPhases[allPhases.length - 1] || null;

  if (!currentPhase) {
    return {
      phase: allPhases[0] || null,
      allPhases,
      status: "on_track",
      message: `Mulailah dari fase pertama: ${allPhases[0]?.phase_name}`,
    };
  }

  if (currentPhase.behind_schedule_signal) {
    return {
      phase: currentPhase,
      allPhases,
      status: "behind_schedule",
      message: currentPhase.behind_schedule_signal,
    };
  }

  if (currentPhase.over_achievement) {
    return {
      phase: currentPhase,
      allPhases,
      status: "ahead_of_schedule",
      message: currentPhase.over_achievement,
    };
  }

  return {
    phase: currentPhase,
    allPhases,
    status: "on_track",
    message: `Kamu berada di fase "${currentPhase.phase_name}". Fokus pada big win: ${currentPhase.big_win_title}`,
  };
}

